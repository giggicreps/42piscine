int	ft_sqrt(int nb)
{
	int	sq;

	sq = 1;
	if (nb == 0)
		return (0);
	while (1)
	{
		if (sq < nb / sq)
			sq++;
		else if (sq * sq == nb)
			return (sq);
		else
			return (0);
	}
}
